export const rgbToHsv = function (arr) {
    let h = 0;
    let s = 0;
    let v = 0;
    let r = arr[0];
    let g = arr[1];
    let b = arr[2];
    arr.sort(function (a, b) {
        return a - b;
    });
    var max = arr[2];
    var min = arr[0];
    v = max / 255;
    if (max === 0) {
        s = 0;
    } else {
        s = 1 - (min / max);
    }
    if (max === min) {
        h = 0;
    } else if (max === r && g >= b) {
        h = 60 * ((g - b) / (max - min)) + 0;
    } else if (max === r && g < b) {
        h = 60 * ((g - b) / (max - min)) + 360;
    } else if (max === g) {
        h = 60 * ((b - r) / (max - min)) + 120;
    } else if (max === b) {
        h = 60 * ((r - g) / (max - min)) + 240;
    }
    h = parseInt(h, 10);
    s = parseInt(s * 100, 10);
    v = parseInt(v * 100, 10);
    return [h, s, v];
};
